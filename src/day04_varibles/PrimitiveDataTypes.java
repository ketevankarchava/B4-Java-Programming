package day04_varibles;

public class PrimitiveDataTypes {

    public static void main(String[] args) {

        byte age = 34; // declared and assigned
        System.out.println(age); //variable, datatype byte
        System.out.println(34); // 34 is a value and BY DEFAULT all the values are INT

        System.out.println("---------");

        short year; // declared
        year = 2024; // assigned
        System.out.println(year); // variable, datatype short
        System.out.println(2024); // 2024 is a value and BY DEFAULT all the values are INT

        System.out.println("---------");

        int ipNumber = 123456; // declared and assigned
        System.out.println(ipNumber); // variable, datatype int
        System.out.println(123456); // 123456 is a value and BY DEFAULT all the values are INT

        System.out.println("---------");

        long accNumber =1234567812345678l;  // declared and assigned
        System.out.println(accNumber); // variable, datatype long
        System.out.println(1234567812345678l); // value type long

    }
}
