package day04_varibles;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("\"iphone\", \"laptop\", \"xbox\", \"webcam\", \"monitor\"");


        System.out.println("iphone\n\tlaptop\nxbox\n\twebcam\nmonitor");

        System.out.println("\tiphone\n\t\tlaptop\n\txbox\n\t\twebcam\n\tmonitor");



    }
}
